public class chara {
    public static void main(String args[]){
        int n=5;
        char ch ='A';
        for(int i=1;i<=n;i++){
            for(int x=1;x<=i;x++){
                System.out.print(ch);
                ch++;
                
            }
            System.out.println();
        }
    }
    
}
